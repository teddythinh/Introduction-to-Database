FROM NHANVIEN NV, PHANCONG PC, LICHBAY LB
WHERE NV.MANV = PC.MANV 
AND PC.MACB = LB.MACB 
AND PC.MACB = '100' 
AND LB.NGAYDI = '2000/11/01'
AND NV.LOAINV = 1

-- CÂU 12
