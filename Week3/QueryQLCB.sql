FROM LOAIMB LMB, MAYBAY MB, LICHBAY LB, PHANCONG PC, NHANVIEN NV
WHERE NV.MANV = PC.MANV 
AND PC.MACB = LB.MACB 
AND PC.NGAYDI = LB.NGAYDI 
AND LB.MALOAI = MB.MALOAI
AND MB.MALOAI = LMB.MALOAI 
AND NV.TEN = 'Quang'
AND NV.LOAINV = 1

